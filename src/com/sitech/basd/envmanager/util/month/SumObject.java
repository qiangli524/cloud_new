package com.sitech.basd.envmanager.util.month;

public class SumObject {

	private String PRODUCT = null;
	private String DOMAIN = null;
	private Object APPLY = null;
	private Object USED = null;
	private Object MEM = null;

	public String getPRODUCT() {
		return PRODUCT;
	}

	public void setPRODUCT(String product) {
		PRODUCT = product;
	}

	public String getDOMAIN() {
		return DOMAIN;
	}

	public void setDOMAIN(String domain) {
		DOMAIN = domain;
	}

	public Object getAPPLY() {
		return APPLY;
	}

	public void setAPPLY(Object apply) {
		APPLY = apply;
	}

	public Object getUSED() {
		return USED;
	}

	public void setUSED(Object used) {
		USED = used;
	}

	public Object getMEM() {
		return MEM;
	}

	public void setMEM(Object mem) {
		MEM = mem;
	}
}
