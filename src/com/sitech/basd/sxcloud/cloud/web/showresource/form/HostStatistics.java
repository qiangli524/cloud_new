package com.sitech.basd.sxcloud.cloud.web.showresource.form;

public class HostStatistics {
	private int allCPU;
	private int usedCPU;
	private int freeCPU;
	private double allMem;
	private double usedMem;
	private double freeMem;
	private double allStor;
	private double usedStor;
	private double freeStor;

	public int getAllCPU() {
		return allCPU;
	}

	public void setAllCPU(int allCPU) {
		this.allCPU = allCPU;
	}

	public int getUsedCPU() {
		return usedCPU;
	}

	public void setUsedCPU(int usedCPU) {
		this.usedCPU = usedCPU;
	}

	public int getFreeCPU() {
		return freeCPU;
	}

	public void setFreeCPU(int freeCPU) {
		this.freeCPU = freeCPU;
	}

	public double getAllMem() {
		return allMem;
	}

	public void setAllMem(double allMem) {
		this.allMem = allMem;
	}

	public double getUsedMem() {
		return usedMem;
	}

	public void setUsedMem(double usedMem) {
		this.usedMem = usedMem;
	}

	public double getFreeMem() {
		return freeMem;
	}

	public void setFreeMem(double freeMem) {
		this.freeMem = freeMem;
	}

	public double getAllStor() {
		return allStor;
	}

	public void setAllStor(double allStor) {
		this.allStor = allStor;
	}

	public double getUsedStor() {
		return usedStor;
	}

	public void setUsedStor(double usedStor) {
		this.usedStor = usedStor;
	}

	public double getFreeStor() {
		return freeStor;
	}

	public void setFreeStor(double freeStor) {
		this.freeStor = freeStor;
	}

}
