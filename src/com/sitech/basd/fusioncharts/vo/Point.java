package com.sitech.basd.fusioncharts.vo;

public class Point {
	private String startvalue;
	private String displayvalue;
	private String usemarker;
	private String markerradius;
	private String dashed;
	private String dashlen;
	private String dashgap;

	public String getStartvalue() {
		return startvalue;
	}

	public void setStartvalue(String startvalue) {
		this.startvalue = startvalue;
	}

	public String getDisplayvalue() {
		return displayvalue;
	}

	public void setDisplayvalue(String displayvalue) {
		this.displayvalue = displayvalue;
	}

	public String getUsemarker() {
		return usemarker;
	}

	public void setUsemarker(String usemarker) {
		this.usemarker = usemarker;
	}

	public String getMarkerradius() {
		return markerradius;
	}

	public void setMarkerradius(String markerradius) {
		this.markerradius = markerradius;
	}

	public String getDashed() {
		return dashed;
	}

	public void setDashed(String dashed) {
		this.dashed = dashed;
	}

	public String getDashlen() {
		return dashlen;
	}

	public void setDashlen(String dashlen) {
		this.dashlen = dashlen;
	}

	public String getDashgap() {
		return dashgap;
	}

	public void setDashgap(String dashgap) {
		this.dashgap = dashgap;
	}

}
